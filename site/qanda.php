<br><br>
<i><b>Q. Is The Freight Depot a freight exchange or auction?</b></i><br>
<i><b>A.</b></i> No, unlike freight exchanges and auctions The Freight Depot takes active control and responsibility for your shipments, from making sure the shipments are picked up to after the shipment is delivered, even if there is a problem, The Freight Depot makes sure that you and your customers are completely satisfied. 
<br><br>
<i><b>Q. What are the advantages to using The Freight Depot?</b></i><br>
<i><b>A.</b></i> The Freight Depot reduces costs, improves service levels, and provides improved information flow to you and your customers.  
<br><br>
<i><b>Q. Who sends me a bill?</b></i><br>
<i><b>A.</b></i> The Freight Depot, one of the advantages in using The Freight Depot is a reduction of freight bills. The Freight Depot will send you one consolidated statement showing your activity for the week, you can chose to receive this regular mail or E-mail. 
<br><br>
<i><b>Q. Do I have to negotiate a discount?</b></i><br>
<i><b>A</b></i>. No, The Freight Depot will provide you with our best net rates. No more trying to figure our base rates, discounting or tariffs. Simply put, you will have a better understanding of what it cost to move a shipment.   
<br><br>
<i><b>Q. What if there is a claim on my shipment?</b></i><br>
<i><b>A.</b></i> The Freight Depot will act on your behalf to settle the claim, using The Freight Depot's on-line claim forms submit the claim to The Freight Depot and we will take care of the rest. Valid claims are processed and paid within 30 days of the day you transmit your claim.   
<br><br>
<i><b>Q. How do I start with The Freight Depot?</b></i><br>
